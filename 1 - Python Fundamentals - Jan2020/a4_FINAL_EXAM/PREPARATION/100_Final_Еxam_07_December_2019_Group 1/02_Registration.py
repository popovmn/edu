import re

success_regs = 0
for inputs in range(int(input())):
    m = re.match(r"U\$([A-Z][a-z]{2,})U\$P@\$([a-z]{5,}[0-9]+)P@\$", input())
    if m:
        print("Registration was successful")
        print(f"Username: {m.group(1)}, Password: {m.group(2)}")
        success_regs += 1
        continue
    print("Invalid username or password")
print(f"Successful registrations: {success_regs}")
