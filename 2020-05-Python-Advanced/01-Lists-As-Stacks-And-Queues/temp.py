class List(list):
    def popleft(self):
        self.remove(self[0])

    def popright(self):
        self.remove(self[-1])


letters = List([input() for person in range(int(input()))])
print(f"list = {letters}")

letters.popleft()
print(letters)

letters.popright()
print(letters)
